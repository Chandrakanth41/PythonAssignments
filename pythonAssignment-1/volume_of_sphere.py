# given diameter = 12cm, so radius is half of diameter i.e r = 6cm
# the value of pie can be taken as 3.142 approx
# formula to find volume of sphere is volume =(4/3)*pie*(r*r*r)

radius=6  
pie_value=3.142 
volume=(4.0/3.0)*pie_value*(radius*radius*radius)  
print("volume of the sphere="+str(volume))  
